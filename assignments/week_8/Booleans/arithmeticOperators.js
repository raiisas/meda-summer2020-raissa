// Arithmetic Operators
2 + 5; // Addition
4 - 3; // Subtraction
5 - 9; // Subtraction and integars
3 * 5; // Mutlipulcations
36 / 6; // Divsion
36 / 5; // Fractions

20 % 2; // Modulas is Remainder
19 % 2;

// Incrementing and Decrementing
// twenty an even number?
20 % 2 === 0; // true

var counter = 0;
counter = counter + 1;
counter;

counter += 1; // Addition Assignment Operator
counter++; // Increments only 1

counter += 5; // += Increments how ever much you want
counter += -4; 

counter -= 1; // Decrementing or going back to the previous value
counter--;
counter;

counter *= 2; // This can work with multiplication and division

// there's some exceptions on how this works on strings but a + is called a Cancatenation Operator
"cat" + "dog";
"cat " + "dog";
"cat" + " and " + "dog";

